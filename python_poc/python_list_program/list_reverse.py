def reversed_method():
    list_container = [1,2,3,4,5]
    print(list(reversed(list_container)))


def reverse_method():
    list_container = [1, 2, 3, 4, 5]
    list_container.reverse()
    print(list_container)

def slicing_tech():
    list_container = [1, 2, 3, 4, 5]
    reverse_list = list_container[ : :-1]
    print(reverse_list)


reversed_method()
reverse_method()
slicing_tech()

